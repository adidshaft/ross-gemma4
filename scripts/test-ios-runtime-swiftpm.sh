  testRuntimeHealthMarksUnsupportedGemma4MultimodalMLXArchiveUnavailable
  testMLXRunReturnsUnsupportedArchiveCategoryBeforeGeneration
  testRuntimeHealthKeepsMLXAvailableWithInvalidDraftArtifactWithoutClaimingAcceleration
