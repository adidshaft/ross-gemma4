  testFoundationProviderReportsEmptyAdapterArtifactsBeforeGeneration
  testFoundationProviderRejectsNonAdapterPathShapeBeforeGeneration
  testRuntimeHealthRejectsCoreMLKindWithNonAdapterPathShape
  testFoundationProviderReportsForeignAdapterArtifactsBeforeGeneration
  testFoundationProviderReportsUnsupportedPlatformBeforeGeneration
)

TEST_SOURCE="$ROOT_DIR/ios/Tests/RossTests/AlphaExtractionTests.swift"
for test_name in "${REQUIRED_TESTS[@]}"; do
  if ! grep -q "func ${test_name}" "$TEST_SOURCE"; then
    echo "Missing required runtime guardrail test: ${test_name}" >&2
    exit 1
  fi
done

RUNTIME_FILTER="AlphaExtractionTests/($(IFS='|'; echo "${REQUIRED_TESTS[*]}"))"

cd "$ROOT_DIR"
echo "Running ${#REQUIRED_TESTS[@]} SwiftPM iOS runtime identity/guardrail tests..."
swift test \
  --package-path ios \
  --scratch-path "$SCRATCH_PATH" \
  --filter "$RUNTIME_FILTER"
