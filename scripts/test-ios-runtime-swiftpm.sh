set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
SCRATCH_PATH="${ROSS_SWIFTPM_SCRATCH_PATH:-$ROOT_DIR/ios/build-test/swiftpm}"

REQUIRED_TESTS=(
  testRuntimeIdentityLineIncludesGGUFDraftBenchmarkFields
  testRuntimeIdentityLineReportsSimulatorGGUFCPUOffload
  testRuntimeEnvironmentParsesPhysicalMemoryOverrideForSimulatorSmoke
  testRuntimeIdentityLineIncludesRejectedGGUFDraftCandidateWithoutClaimingAcceleration
  testRuntimeIdentityLineIncludesMLXDirectoryFields
  testRuntimeIdentityLineIncludesUnavailableMLXDraftCandidate
  testRuntimeIdentityLineIncludesCoreAISystemFields
  testRuntimeIdentityLineUsesActualUnavailableCoreAIProviderName
  testRuntimeIdentityPreflightProviderNamesStayRuntimeSpecific
  testMissingActivePackErrorsStayRuntimeSpecific
  testRuntimeIdentityLineMarksDeterministicProviderAsFallback
  testAskResultPreservesModelInvocationForAnswerDetails
  testAnswerDetailOverviewMetricsPreferMeasuredTokenCountLabel
  testAnswerDetailOverviewMetricsMarkEstimatedTokenSpeedAsApproximate
  testAnswerDetailOverviewMetricsIncludeSourceCoverageWhenAvailable
  testRuntimeHealthRejectsMLXRequestAgainstGGUFPackBeforeFallback
  testRuntimeHealthRejectsCoreAIRequestAgainstGGUFPackBeforeFallback
  testExplicitMLXRuntimeRequestDoesNotFallBackToGGUFProvider
  testExplicitCoreMLRuntimeRequestDoesNotFallBackToGGUFProvider
  testRuntimeIdentityLineIncludesMissingCoreAIArtifactForExplicitCoreMLWithoutSentinel
  testExplicitCoreMLRuntimeRequestRejectsForeignDebugPathWithoutKindBeforeProviderConstruction
  testRuntimeHealthRejectsFileBackedSystemKindBeforeProviderConstruction
  testRuntimeIdentityLineIncludesMissingMLXArtifactForExplicitMLXWithoutBorrowingGGUF
  testDebugLocalModelSmokePackRejectsMissingRuntimeArtifactKind
  testExplicitCoreAIAdapterPathOverridesActiveSystemSentinel
  testInstalledModelSmokePackTargetsExactPackIDOverride
  testInstalledModelSmokePackPrefersRequestedRuntimeOverActiveFallback
  testInstalledModelSmokePackFailsClosedForMissingRequestedRuntime
  testExperimentalGGUFProviderAllowsLowTokenDraftAccelerationForConstrainedQuickStartE4B
