  testExperimentalGGUFProviderDoesNotPassInvalidDraftArtifactToGenerationContext
  testRuntimeHealthMarksConfiguredGGUFFileAsMissingMLXArtifact
  testPreparedMLXRuntimeDirectoryPatchesGemma4SharedKVWeightMap
